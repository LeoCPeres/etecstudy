        <h1>O Que É O Enem E Como Funciona?</h1>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">O Exame Nacional do Ensino Médio (Enem) é uma prova anual que
            tem o objetivo de avaliar a qualidade do Ensino Médio em escolas de todo o País. <strong>Ele é a principal
                porta de acesso a cursos em universidades públicas e privadas.</strong> Além disso, essa prova garante a
            estudantes de baixa renda o acesso aos programas governamentais de bolsas de estudo ou financiamento com
            juros reduzidos.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Organizado pelo Ministério da Educação, <strong>esse exame é
                composto por 180 questões e uma redação.</strong> Os conteúdos abordados envolvem o que o aluno aprender
            nos três anos do Ensino Médio, divididos nas seguintes áreas:
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <ul style="font-size: 20px; text-align: justify;">
            <li>
                Ciências Humanas e suas Tecnologias: História, Geografia, Filosofia e Sociologia
                <div class="col-sm-12">&nbsp;</div>
            </li>
            <li>
                Ciências da Natureza e suas Tecnologias: Química, Física e Biologia
                <div class="col-sm-12">&nbsp;</div>
            </li>
            <li>
                Linguagens, Códigos e suas Tecnologias e Redação: Língua Portuguesa, Literatura, Língua Estrangeira
                (Inglês ou Espanhol), Artes, Educação Física e Tecnologias da Informação e Comunicação
                <div class="col-sm-12">&nbsp;</div>
            </li>
            <li>
                Matemática e suas Tecnologias: Matemática
                <div class="col-sm-12">&nbsp;</div>
            </li>
        </ul>
        <h1>Como saber o local da prova do Enem?</h1>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Cerca de um mês antes da data de realização da prova, o Inep,
            instituto que aplica o exame, divulga o local de realização da prova. Em geral, <strong>o candidato recebe a
                notificação no e-mail cadastrado no ato da inscrição</strong>, mas é possível acompanhar essa
            atualização na <a href="https://enem.inep.gov.br/participante/#!/">área do
                participante</a>, dentro do portal do Inep.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Para acessar essa área, será preciso entrar no site do
            instituto e fazer o login com o CPF. Ali, o estudante terá acesso a informações importantes, tais como nome,
            número de inscrição e local de prova.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">O <strong>local de prova do Enem 2021 já foi
                divulgado</strong>, não deixe de conferir para se organizar do dia do exame e chegar a tempo.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <h1>Qual a data do Enem 2021?</h1>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">A edição regular de 2021 do Enem acontece nos dias 21 e 28 de
            novembro. Diferente da última edição, este ano, a modalidade impressa e digital serão aplicadas no mesmo
            dia.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Estima-se que o a divulgação dos resultados do exame seja feita
            em janeiro de 2022, porém vale a pena sempre acompanhar as atualizações na Página do Participante, no site
            do Inep, para ficar por dentro de todas as novidades.


        </p>

        <div class="col-sm-12">&nbsp;</div>
        <h1>Quais são as vantagens de fazer o Enem?</h1>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">O Enem traz uma série de vantagens aos candidatos. A principal
            delas é <strong>viabilizar a entrada em universidades sem a necessidade de fazer o vestibular.</strong> Além
            disso, ele dá <strong>acesso a programas como o Sistema de Seleção Unificada (SiSu), que oferece vagas em
                universidades públicas com base na nota obtida no exame.</strong>
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">O acesso a faculdades particulares também é beneficiado pelo
            Enem. <strong>É com base nesta nota que o Programa Universidade para Todos (ProUni) concede bolsas de
                estudos</strong> parciais (50%) e integrais (100%) para estudantes brasileiros que ainda não tenham um
            diploma de Ensino Superior. O FIES, programa de financiamento estudantil do Governo Federal, <strong>permite
                que o
                candidato financie entre 50 e 100% das mensalidades da faculdade particular</strong> a juros baixos –
            tudo com base
            na nota do Enem.


        </p>

        <div class="col-sm-12">&nbsp;</div>
        <h1>Por que o Enem é importante para os estudantes?</h1>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">A prova explora diversas áreas do conhecimento e aplica o
            conceito de interdisciplinaridade na elaboração da prova. Assim, os estudantes que realizam o exame têm o
            desafio de demonstrar sua capacidade de raciocínio lógico e interpretação, deixando o famoso “decoreba” de
            lado.</strong>
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Além disso, ele prevê que o candidato seja capaz de desenvolver
            uma redação com elementos de dissertação, desenvolvendo seus argumentos e defendendo seu ponto de vista.
            <strong>Quando obtém uma boa nota no Enem, o estudante está credenciado para ingressar em universidades
                públicas e
                privadas concorridas.</strong> Ele obtém uma espécie de “passaporte” com o Enem, que dá acesso,
            inclusive, a algumas
            universidades no exterior.


        </p>

        <div class="col-sm-12">&nbsp;</div>
        <h1>O que acontece se eu passar na prova do Enem?</h1>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Participar do Enem não significa concorrer automaticamente a
            uma vaga na faculdade. Se você conseguir uma boa nota no exame, <strong>é preciso ficar atento aos
                cronogramas dos
                processos seletivos e se inscrever.</strong> Com essa nota, você pode entrar em uma universidade sem
            fazer o
            vestibular, conseguir bolsas de estudo e até financiar uma faculdade privada.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;"><strong>Programas como SiSu, ProUni e FIES utilizam a nota do
                Enem para selecionar os candidatos que têm direito ao benefício.</strong> Por isso, se você passar no
            Enem, é preciso ficar atento à data de inscrição para não perder a oportunidade. Faculdades privadas e
            públicas também abrem seus processos seletivos após a realização do exame, então, vale ficar atento!


        </p>

        <div class="col-sm-12">&nbsp;</div>
        <h1>Como funciona o sistema do Enem para entrar na faculdade?</h1>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">A nota do Enem dá acesso a diversos programas estudantis do
            Governo e deve ser utilizada nos vestibulares de universidades públicas e privadas. Grande parte das
            instituições, inclusive, utiliza apenas a nota do exame para selecionar os candidatos, dispensando o
            vestibular.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">O sistema de avaliações do Enem credencia os estudantes a se
            candidataram a programas como SiSu, ProUni e FIES. Além disso, permite que a nota do vestibular seja
            complementada e que o candidato entre em universidades particulares sem a necessidade de vestibular.


        </p>

