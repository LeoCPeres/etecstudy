        <h1>Como Fazer A Inscrição No Enem?</h1>

        <div class="col-sm-12">&nbsp;</div>

        <p style="font-size: 20px; text-align: justify;">Para se inscrever no Enem, <strong>basta acessar
                o</strong> <a href="https://enem.inep.gov.br/participante/#!/">site
                da prova</a> <strong>e preencher seus dados pessoais</strong> (CPF, data de nascimento, RG e endereço).
            Depois, é preciso
            criar
            uma senha, que será utilizada durante todo o processo de realização e obtenção de resultados do exame. </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Neste momento, o candidato especifica se tem alguma necessidade
            especial, escolhe o idioma para a prova de língua estrangeira e responde ao questionário socioeconômico, com
            questões sobre renda e grupo familiar. Ao final desse processo, <strong>o candidato deve imprimir o boleto e
                fazer o
                pagamento até a data de vencimento especificada</strong> no documento gerado.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <h1>Como saber se minha inscrição do Enem foi confirmada 2021?</h1>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">O Inep orienta que os estudantes que efetuaram o pagamento e
            ainda aguardam sua confirmação acessem a Página do Participante. <strong>Ali, o sistema deve informar
                “Inscrição concluída com sucesso” após alguns dias do pagamento, que será processado pelo
                banco.</strong>
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Se após alguns dias da confirmação bancária o status ainda não
            for alterado, entre em contato com o Ministério da Educação (MEC) pelo telefone 0800 616161.</strong>
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <h1>Quem pode se inscrever para fazer a prova do Enem?</h1>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Em teoria, o <strong>Enem pode ser feito por qualquer pessoa
                com condições de responder a prova, de qualquer faixa etária.</strong> Entretanto, apenas poderá usar
            sua nota quem estiver concluindo o Ensino Médio ou já o tenha concluído. Quem ainda estiver no segundo ano
            pode fazer o exame, mas na condição de treineiro.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Pessoas com necessidades especiais e detentos, por exemplo,
            também podem fazer o exame, especificando no ato da inscrição essa necessidade específica para que a prova
            seja aplicada de forma diferenciada.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <h1>Como acompanhar a inscrição do Enem pelo gov BR?</h1>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;"><strong>Os candidatos que se inscreverem no Enem precisam
                também se cadastrar no portal gov.br</strong>, que reúne diversos serviços sobre o cidadão e informações
            sobre o exame. Além desse portal, <strong>o estudante pode acompanhar sua inscrição pela Página do
                Participante do Enem</strong>, clicando no botão “GOV.BR”, localizado no canto superior do site.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Nesse momento, o candidato precisará informar CPF, nome
            completo, e-mail e telefone, além e cadastrar login e senha. A partir daí, ele poderá acompanhar sua
            inscrição e todos os resultados por essa plataforma, que agora integra todos os sites do Governo Federal.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <h1>Como entrar na página do participante Enem 2021?</h1>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Acessar a página do participante Enem é muito fácil. Basta
            apenas <a href="https://enem.inep.gov.br/participante/#!/inicial">entrar no portal</a> do Inep e clicar em
            “Página do Participante”.
        </p>
        <div class="col-sm-12">&nbsp;</div>
        <p style="font-size: 20px; text-align: justify;">Na sequência, é preciso responder ao desafio de segurança,
            identificando a figura solicitada, e inserindo o número do seu CPF e a senha que você cadastrou na hora de
            fazer a inscrição. Feito isso, clique em “Enviar” e pronto. Você pode, ainda, acessar a página do
            participante por meio do aplicativo do Enem, disponível para Android e iOS.
        </p>
