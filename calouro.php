<h3>Mas afinal, qual a diferença entre os cursos?</h3>

<div class="col-sm-12">&nbsp;</div>

<h4>Novotec Integrado - (PI)</h4>

<p>A modalidade de curso "Novotec Integrado - (PI ou Periodo Integral)" é composta por uma rotina com até oito aulas
    diárias em período
    integral.Assim, a matriz curricular é capaz de contar com projetos de aprofundamento voltados às áreas do
    conhecimento mais exigidas no mercado de trabalho. </p>
<p>Ao completar as três séries, o aluno recebe o diploma de técnico
    que lhe dará o direito de exercer a habilitação profissional e de prosseguir os estudos no nível da educação
    superior.</p>
<div class="col-sm-12">&nbsp;</div>
<h4>Novotec Integrado - (M-Tec)</h4>

<p>A modalidade de curso "Novotec Integrado - (M-Tec ou Médio Técnico)" é composta por uma rotina de seis aulas diárias
    em meio período (manhã ou tarde).</p>
<p>A matriz curricular mescla disciplinas da base nacional comum ao Ensino Médio com componentes do Ensino Técnico. Há a
    possibilidade, também, de oferta no período noturno, com parte da carga horária ministrada por intermédio de
    atividades não presenciais.</p>
<p>Ao completar as três séries, o aluno terá concluído o Ensino Médio e obterá o diploma de Técnico, que lhe dará o
    direito de exercer a habilitação profissional e de prosseguir os estudos no nível da Educação Superior.</p>
<div class="col-sm-12">&nbsp;</div>
<h4>Articulação da Formação Profissional Média e Superior (AMS)</h4>

<p>Nessa modalidade, o aluno é submetido à cinco anos de formação, integrando o Ensino Médio (técnico) e Superior
    (tecnológico).</p>
<p>O aluno ingressa no Ensino Médio com Habilitação Profissional de Técnico no curso escolhido e, depois de concluir um
    ciclo de três anos, pode completar o curso superior tecnológico na mesma área com mais dois anos de estudo.</p>
<div class="col-sm-12">&nbsp;</div>
<h4>Cursos Técnicos Presenciais</h4>
<p>As Escolas Técnicas Estaduais (Etecs) oferecem mais de 100 opções de cursos técnicos presenciais com duração média de
    1200 horas, divididas em três semestres.</p>
<p>O ingresso é feito mediante processo seletivo semestral (Vestibulinho). No momento da inscrição é preciso conferir
    quais os cursos oferecidos em cada unidade.</p>